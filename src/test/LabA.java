package test;

public class LabA {

	public static int add(int a, int b) {
		return a + b;
	}
	
	public static int subtract(int a, int b) {
		return a - b;
	}
	
	public static int divide(int a, int b) {
		return a / b;
	}
	
	public static int multiply(int a, int b) {
		return a * b;
	}
	
//	circumference
	public static double circ(double diameter) {
		double pi = 3.14D;
		return pi * diameter;
	}
}
